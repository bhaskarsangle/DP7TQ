package com.operatorProgram;

public class LeftRightShiftOperator {

	public static void main(String[] args) {
 
		//LeftShiftOperator
		System.out.println(10<<1);    //Formula no*2^no of shift
		System.out.println(10<<2);   //10*2^2=40
		
		
		//RightShift Operator     
		System.out.println(10>>1); //formula no/2^no of shift
		System.out.println(10>>2);  //10/2^2=2

	}

}
